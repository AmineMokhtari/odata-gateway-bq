-- 1. Create the dataset (schema) if it does not exist
CREATE SCHEMA IF NOT EXISTS `home_insurance_v1`
OPTIONS (
  location = 'europe-west1',
  description = 'Data product delivering curated, analytical-ready Home Insurance Policy information. Conforms to standard industry models.'
);

-- 2. Create the core Policies Table
CREATE TABLE IF NOT EXISTS `home_insurance_v1.policies` (
  policy_id STRING OPTIONS(description="Globally unique identifier (UUID) for the policy instance"),
  policy_number STRING OPTIONS(description="Unique business key representing the policy contract number"),
  customer_id STRING OPTIONS(description="Unique business identifier for the policyholder/client"),
  effective_date DATE OPTIONS(description="The date the policy coverage becomes active"),
  expiration_date DATE OPTIONS(description="The date the policy coverage expires"),
  status STRING OPTIONS(description="Current operational state of the policy (e.g., ACTIVE, CANCELLED, LAPSED)"),
  total_premium NUMERIC OPTIONS(description="The total gross premium written for the full policy term in contract currency"),
  currency STRING OPTIONS(description="ISO 4217 three-letter currency code"),
  created_at TIMESTAMP OPTIONS(description="Operational timestamp when the policy was minted in the upstream source"),
  PRIMARY KEY (policy_id) NOT ENFORCED
)
OPTIONS (
  description = 'Core dimension table representing active and historical home insurance policy contracts.'
);

-- 3. Create the Insured Properties Table
CREATE TABLE IF NOT EXISTS `home_insurance_v1.insured_properties` (
  property_id STRING OPTIONS(description="Globally unique identifier (UUID) for the insured property location"),
  policy_id STRING OPTIONS(description="Foreign key linking the physical property to its underlying contract"),
  address_line1 STRING OPTIONS(description="Primary street address of the insured home"),
  city STRING OPTIONS(description="The city where the insured risk is located"),
  postal_code STRING OPTIONS(description="The postal code or ZIP code of the risk location"),
  country STRING OPTIONS(description="ISO 3166-1 alpha-2 country code of the property"),
  construction_year INT64 OPTIONS(description="The calendar year the property was originally constructed"),
  property_type STRING OPTIONS(description="Risk classification of the building structure (e.g., SINGLE_FAMILY, APARTMENT, DUPLEX)"),
  estimated_value NUMERIC OPTIONS(description="Estimated reconstruction value (Replacement Cost Value) in contract currency"),
  PRIMARY KEY (property_id) NOT ENFORCED,
  CONSTRAINT fk_properties_policy FOREIGN KEY (policy_id) REFERENCES `home_insurance_v1.policies`(policy_id) NOT ENFORCED
)
OPTIONS (
  description = 'Risk characteristics and location metrics of the physical properties covered by the policies.'
);

-- 4. Create the Coverages Table
CREATE TABLE IF NOT EXISTS `home_insurance_v1.coverages` (
  coverage_id STRING OPTIONS(description="Globally unique identifier (UUID) for the individual coverage line item"),
  policy_id STRING OPTIONS(description="Foreign key linking this specific coverage limit to its parent policy"),
  coverage_type STRING OPTIONS(description="Specific protection bucket (e.g., DWELLING, PERSONAL_PROPERTY, LIABILITY, LOSS_OF_USE)"),
  limit_amount NUMERIC OPTIONS(description="Maximum indemnity amount payable for a covered loss under this specific line"),
  deductible_amount NUMERIC OPTIONS(description="The out-of-pocket amount the policyholder pays before carrier coverage starts"),
  PRIMARY KEY (coverage_id) NOT ENFORCED,
  CONSTRAINT fk_coverages_policy FOREIGN KEY (policy_id) REFERENCES `home_insurance_v1.policies`(policy_id) NOT ENFORCED
)
OPTIONS (
  description = 'Detailed breakdown of indemnity limits and deductibles per policy itemization.'
);


