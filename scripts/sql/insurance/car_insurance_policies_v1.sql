-- ==============================================================================
-- DATA PRODUCT: Car Insurance Policies
-- BUSINESS OBJECT: Car Insurance Policy
-- DATASET VERSION: car_insurance_policies_v1
-- TARGET REGION: europe-west1
-- DESCRIPTION: Standard insurance industry data model containing policy details,
--              insured vehicles, named drivers, and specific coverage terms.
-- ==============================================================================

-- 1. Create the Dataset (Schema) if it does not exist in the europe-west1 region
CREATE SCHEMA IF NOT EXISTS `car_insurance_policies_v1`
OPTIONS (
  location = 'europe-west1',
  description = 'Car Insurance Policy Data Product. Houses policy contracts, vehicle details, driver profiles, and coverages.'
);

-- 2. Create tables with descriptions, data types, and key constraints

-- A. Core Policies Table
CREATE TABLE IF NOT EXISTS `car_insurance_policies_v1.policies` (
  policy_id STRING NOT NULL OPTIONS(description = "Primary Key. Unique surrogate key representing a specific policy instance."),
  policy_number STRING NOT NULL OPTIONS(description = "Unique natural business key assigned to the insurance policy contract."),
  inception_date DATE NOT NULL OPTIONS(description = "The effective start date of the insurance coverage policy."),
  expiry_date DATE NOT NULL OPTIONS(description = "The planned expiration date of the insurance coverage policy."),
  policy_status STRING NOT NULL OPTIONS(description = "Current state of the policy. Permitted values: ACTIVE, CANCELLED, LAPSED, EXPIRED."),
  premium_amount NUMERIC NOT NULL OPTIONS(description = "The total premium calculated for the current term of the policy."),
  currency STRING NOT NULL OPTIONS(description = "Three-letter ISO currency code representing the premium currency. Standardized to EUR."),
  payment_frequency STRING NOT NULL OPTIONS(description = "The schedule of premium payments. e.g., MONTHLY, ANNUALLY, QUARTERLY."),
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP() OPTIONS(description = "Audit timestamp showing when the policy record was first created in the system."),
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP() OPTIONS(description = "Audit timestamp showing the last update date of the policy record."),
  PRIMARY KEY (policy_id) NOT ENFORCED
)
OPTIONS (
  description = "Contains core insurance policy contract records, capturing the life cycle states, terms, and total premiums."
);

-- B. Insured Vehicles Table
CREATE TABLE IF NOT EXISTS `car_insurance_policies_v1.insured_vehicles` (
  vehicle_id STRING NOT NULL OPTIONS(description = "Primary Key. Unique identifier of the insured vehicle asset."),
  policy_id STRING NOT NULL OPTIONS(description = "Foreign Key referencing the primary policy holding the asset coverage."),
  vin STRING NOT NULL OPTIONS(description = "Vehicle Identification Number (VIN) of the covered vehicle."),
  make STRING NOT NULL OPTIONS(description = "The manufacturer/brand of the vehicle (e.g., Volkswagen, BMW, Peugeot)."),
  model STRING NOT NULL OPTIONS(description = "The specific commercial model name of the vehicle."),
  production_year INT64 NOT NULL OPTIONS(description = "The manufacturing year of the vehicle."),
  registration_number STRING NOT NULL OPTIONS(description = "The license plate or registration number of the vehicle."),
  estimated_value NUMERIC OPTIONS(description = "The estimated current market value of the vehicle in EUR."),
  PRIMARY KEY (vehicle_id) NOT ENFORCED,
  FOREIGN KEY (policy_id) REFERENCES `car_insurance_policies_v1.policies`(policy_id) NOT ENFORCED
)
OPTIONS (
  description = "Stores details of the vehicles associated and covered under specific policy contracts."
);

-- C. Policy Drivers Table
CREATE TABLE IF NOT EXISTS `car_insurance_policies_v1.policy_drivers` (
  driver_id STRING NOT NULL OPTIONS(description = "Primary Key. Unique identifier for the driver profile."),
  policy_id STRING NOT NULL OPTIONS(description = "Foreign Key referencing the primary policy under which the driver is named."),
  first_name STRING NOT NULL OPTIONS(description = "First name of the named driver."),
  last_name STRING NOT NULL OPTIONS(description = "Last name of the named driver."),
  birth_date DATE NOT NULL OPTIONS(description = "The date of birth of the named driver (used for actuarial risk calculations)."),
  license_number STRING NOT NULL OPTIONS(description = "The driver license identifier of the individual."),
  relationship_to_policyholder STRING NOT NULL OPTIONS(description = "The relationship of the driver to the main policyholder (e.g., SELF, SPOUSE, CHILD)."),
  PRIMARY KEY (driver_id) NOT ENFORCED,
  FOREIGN KEY (policy_id) REFERENCES `car_insurance_policies_v1.policies`(policy_id) NOT ENFORCED
)
OPTIONS (
  description = "Contains the personal information of drivers permitted to operate the insured vehicles under the policy."
);

-- D. Policy Coverages Table
CREATE TABLE IF NOT EXISTS `car_insurance_policies_v1.policy_coverages` (
  coverage_id STRING NOT NULL OPTIONS(description = "Primary Key. Unique identifier for the coverage record."),
  policy_id STRING NOT NULL OPTIONS(description = "Foreign Key referencing the parent policy."),
  vehicle_id STRING NOT NULL OPTIONS(description = "Foreign Key referencing the specific vehicle to which the coverage limit is applied."),
  coverage_type STRING NOT NULL OPTIONS(description = "The category of risk protected (e.g., THIRD_PARTY, COLLISION, COMPREHENSIVE, THEFT)."),
  limit_amount NUMERIC NOT NULL OPTIONS(description = "The maximum payout limit specified under this exact coverage."),
  deductible_amount NUMERIC NOT NULL OPTIONS(description = "The self-insured retention / deductible amount to be paid by the client before coverage kicks in."),
  PRIMARY KEY (coverage_id) NOT ENFORCED,
  FOREIGN KEY (policy_id) REFERENCES `car_insurance_policies_v1.policies`(policy_id) NOT ENFORCED,
  FOREIGN KEY (vehicle_id) REFERENCES `car_insurance_policies_v1.insured_vehicles`(vehicle_id) NOT ENFORCED
)
OPTIONS (
  description = "Granular detail of individual coverage items, limits, and deductible levels configured for each vehicle under the policy contract."
);


