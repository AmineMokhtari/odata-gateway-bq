-- =====================================================================
-- BUSINESS OBJECT: Commercial Underwriting Risk Assessment
-- DATA PRODUCT / DATASET: underwriting_risk_v1
-- TARGET REGION: europe-west1 (Belgium)
-- DESCRIPTION: Core tables for automated insurance underwriting,
--              integrating firmographics, geospatial hazard indexes,
--              and analytical scoring engines.
-- =====================================================================

-- 1. Create the Dataset (Schema) if it does not exist
CREATE SCHEMA IF NOT EXISTS `underwriting_risk_v1`
OPTIONS (
  location = 'europe-west1',
  description = 'Data product managing risk profiles, geospatial vulnerability, and automated underwriting decisions for commercial properties.'
);

-- 2. Create the Applications Table (First-Party Baseline Data)
CREATE TABLE IF NOT EXISTS `underwriting_risk_v1.commercial_applications` (
  application_id STRING OPTIONS(description = "Globally unique identifier (UUID) for the policy application"),
  business_name STRING OPTIONS(description = "Registered legal name of the applicant enterprise"),
  industry_code STRING OPTIONS(description = "NACE Rev. 2 / NAICS equivalent industry classification code"),
  annual_revenue NUMERIC OPTIONS(description = "Self-reported annual gross revenue of the enterprise in EUR"),
  employee_count INT64 OPTIONS(description = "Total full-time equivalent (FTE) employees"),
  credit_score INT64 OPTIONS(description = "Commercial credit score sourced from credit bureaus, scaled 300 to 850"),
  established_date DATE OPTIONS(description = "The official incorporation date of the business entity"),
  created_at TIMESTAMP OPTIONS(description = "Timestamp when the application was formally submitted"),
  PRIMARY KEY (application_id) NOT ENFORCED
)
OPTIONS (
  description = 'Captures first-party firmographic and operational parameters submitted during the commercial property insurance application phase.'
);

-- 3. Create the Geospatial Hazards Table (Location-Based Physical Risk Profile)
CREATE TABLE IF NOT EXISTS `underwriting_risk_v1.geospatial_hazards` (
  hazard_id STRING OPTIONS(description = "Unique identifier for the geospatial analysis record"),
  application_id STRING OPTIONS(description = "Foreign key linking back to the submission application"),
  latitude FLOAT64 OPTIONS(description = "WGS84 decimal latitude coordinates of the primary commercial asset"),
  longitude FLOAT64 OPTIONS(description = "WGS84 decimal longitude coordinates of the primary commercial asset"),
  flood_risk_index FLOAT64 OPTIONS(description = "Modelled probability score of flood damage, normalized scale $0.0$ (No Risk) to $10.0$ (Extreme)"),
  wildfire_risk_index FLOAT64 OPTIONS(description = "Modelled wildfire hazard index score based on local canopy density and climate history, scaled $0.0$ to $10.0$"),
  seismic_hazard_class STRING OPTIONS(description = "Seismic hazard zone classification: LOW, MEDIUM, HIGH, VERY_HIGH"),
  PRIMARY KEY (hazard_id) NOT ENFORCED,
  CONSTRAINT fk_geo_application FOREIGN KEY (application_id) REFERENCES `underwriting_risk_v1.commercial_applications`(application_id) NOT ENFORCED
)
OPTIONS (
  description = 'Aggregates secondary geospatial data points reflecting natural catastrophe vulnerabilities based on geographic coordinates.'
);

-- 4. Create the Firmographic History Table (Financial & Credit Health Track Record)
CREATE TABLE IF NOT EXISTS `underwriting_risk_v1.firmographic_history` (
  history_id STRING OPTIONS(description = "Unique identifier for the historical financial verification log"),
  application_id STRING OPTIONS(description = "Foreign key linking to the parent commercial application"),
  tax_lien_indicator BOOL OPTIONS(description = "Boolean indicator of active or unresolved state/national tax liens"),
  bankruptcy_history_count INT64 OPTIONS(description = "The total number of legal bankruptcy filings on record for the business owners or entity"),
  years_in_business INT64 OPTIONS(description = "Derived years of consecutive operation"),
  industry_risk_tier INT64 OPTIONS(description = "Underwriting risk tier assigned to the industry classification, scaled 1 (Low Risk) to 5 (Prohibited)"),
  PRIMARY KEY (history_id) NOT ENFORCED,
  CONSTRAINT fk_firm_application FOREIGN KEY (application_id) REFERENCES `underwriting_risk_v1.commercial_applications`(application_id) NOT ENFORCED
)
OPTIONS (
  description = 'Maintains retrospective financial and public-record health parameters to inform long-term commercial liability risk.'
);

-- 5. Create the Underwriting Decisions Table (The Engine Target / Core Output)
CREATE TABLE IF NOT EXISTS `underwriting_risk_v1.underwriting_decisions` (
  decision_id STRING OPTIONS(description = "Unique identifier generated by the automated decisioning rules engine"),
  application_id STRING OPTIONS(description = "Foreign key linking directly to the evaluated application"),
  composite_risk_score INT64 OPTIONS(description = "Consolidated score generated by weighting geospatial, credit, and firmographic risk profiles. Scaled 1 to 100"),
  projected_loss_ratio NUMERIC OPTIONS(description = "The predicted ratio of claims paid to premiums earned, modeled as a percentage"),
  straight_through_processing_eligible BOOL OPTIONS(description = "Flag indicating if the policy meets criteria for automated approval without human intervention"),
  decision_status STRING OPTIONS(description = "Final engine response output: AUTO_APPROVED, REFER_TO_UNDERWRITER, DECLINED"),
  calculated_at TIMESTAMP OPTIONS(description = "Execution timestamp of the automated scoring engine pass"),
  PRIMARY KEY (decision_id) NOT ENFORCED,
  CONSTRAINT fk_dec_application FOREIGN KEY (application_id) REFERENCES `underwriting_risk_v1.commercial_applications`(application_id) NOT ENFORCED
)
OPTIONS (
  description = 'Represents the actionable final evaluation generated by the underwriting rules engine, driving automated conversion metrics.'
);


